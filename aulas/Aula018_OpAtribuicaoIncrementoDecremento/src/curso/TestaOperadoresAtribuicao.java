package curso;

public class TestaOperadoresAtribuicao {
	public static void main(String[] args) {
		//declaração das variáveis
		int x, y;
		x = 10;
		y = 5;
		
		//x = x + y;
		x += y;
		System.out.println(x);
		x = 10;
		//x = x - y;
		x -= y;
		System.out.println(x);
		x = 10;
		//x = x * y;
		x *= y;
		System.out.println(x);
		x = 10;
		//x = x / y;
		x /= y;
		System.out.println(x);
		
	}

}
